public class forloop 
{
    public static void main(String a[])
    {
        for (int i=1;i<=4;i++) //in for loop we can "intialize,condition,increment" three statements in single one
        {                           //it is not possible with the while loop thats the major diiference
            System.out.println("hi"+i);
        }
    }
    
}
